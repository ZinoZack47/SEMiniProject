package com.ensa.glminiprojectsp.DAO;

public class Major {
    int code;
    String name;

    public Major() { }

    public Major(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
